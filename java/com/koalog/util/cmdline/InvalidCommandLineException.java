package com.koalog.util.cmdline;

/**
 * This exception is thrown when the command line is invalid.
 * @author Yan Georget
 */
public class InvalidCommandLineException extends Exception {
    //------------------------------------------------------------------------
    // CONSTRUCTORS
    //------------------------------------------------------------------------ 
    /**
     * Main constructor.
     */
    public InvalidCommandLineException() {
        super();
    }

    /**
     * Auxilliary constructor.
     * @param message a string
     */
    public InvalidCommandLineException(String message) {
        super(message);
    }
}
