package com.koalog.util.swing.control;

/**
 * The interface to all object editor panels meant to be used
 * in the <CODE>TreeEditor</CODE> control. Also, don't forget
 * that an object editor panel must be a <CODE>Component</CODE>
 * derivative.
 *
 * <P>For better performances, <CODE>ObjectEditor</CODE> instances
 * should be shared as much as possible. That is, there should not
 * be a one to one relationship between the object instances to edit
 * and the editor instances.</P>
 *
 * @author Matthieu Philip
 */
public interface ObjectEditor {
    /**
     * Set the current object. The current object is the object edited
     * by this control instance. When this method is invoked, all the 
     * fields of the control are updated with the passed object attributes.
     * @param object The object to edit.
     */
    public void setObject(Object object);
}
