package com.koalog.util.zip;

/**
 * Exception thrown when a zip entry is expected but not found in an
 * archive.
 * @author Matthieu Philip
 */
public class NoSuchEntryException extends Exception {

    /**
     * Construct the exception with an explanation message.
     * @param message The exception explanation.
     */
    public NoSuchEntryException(String message) {
        super(message);
    }
}
